#!/usr/bin/env python3


# we declare a variable that holds a string:
mytext = "Hello world"


# the print command prints the string-content
# of the variable:
print(mytext)


# strings are sequences of characters, i.e.
# they have a beginning, and end, and a length:
# len( somestring ) returns the length of the
# string. The following command prints the length
# of the string that mytext refers to:
print( len(mytext) )


